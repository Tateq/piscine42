#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	a;
	long	n;

	n = nb;
	if (n < 0)
	{
		write(1, "-", 1);
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
	{
		a = n + '0';
		write(1, &a, 1);
	}
}

