#include <unistd.h>

int	main(int ac, char *av[])
{
	char	a;
	int		i;

	i = 0;
	(void) ac;
	while (av[0][i] != '\0')
	{
		a = av[0][i];
		write (1, &a, 1);
		i++;
	}
	write (1, "\n", 1);
}
